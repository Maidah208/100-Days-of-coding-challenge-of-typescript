function compoundOperators(x: number){
    console.log(`Initial Value: ${x}`)

    x += 2;
    console.log(`After Addition: ${x}`)

    x -= 1
    console.log(`After Subtraction: ${x}`)

    x *= 2
    console.log(`After Multiplication: ${x}`)

    x /= 2
    console.log(`After Division: ${x}`)
}
compoundOperators(4)

